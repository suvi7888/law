                    <p class="mrg-top-20">PT Serasi Autoraya (SERA) merupakan anak usaha PT Astra International Tbk di divisi Infrastruktur dan Logistik. SERA mengawali perjalanan bisnisnya pada tanggal 22 Maret 1990 sebagai perseroan penyedia Jasa Penyewaan Kendaraan dengan merek dagang “Toyota Rent A Car” yang fokus menggarap pasar korporasi. Pada tanggal 5 Oktober 2001, SERA yang pada saat itu 99,9975% sahamnya dimiliki oleh PT Astra International Tbk, melakukan transformasi brand-nya menjadi “TRAC-Astra Rent a Car (TRAC)”. </p>
